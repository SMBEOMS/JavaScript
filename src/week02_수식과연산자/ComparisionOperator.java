package week02_수식과연산자;

public class ComparisionOperator {

	public static void main(String[] args) {
		//관계 연산자
		
		int x = 3;
		int y = 4;
		
		System.out.println((x == y));
		System.out.println(x != y);
		System.out.println(x > y);
		System.out.println(x < y);
		System.out.println(x <= y);
	}

}
