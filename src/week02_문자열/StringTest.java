package week02_문자열;

public class StringTest {

	public static void main(String[] args) {
		//문자열
		// 자바에서 문자열(string)은 문자들의 모임이다. 예를 들어서 문자열 "Hello"는 H,e,l,l,o등의 5개의 유니코드 문자로 구성되어 있다.
		// String 클래스가 제공된다
		String s1 = "Hello World!";
		String s2 = "I'm a new Java programmer";
		
		System.out.println(s1 + "\n" + s2); // + 연산자로 문자열을 합칠 수 있다.
	}

}
